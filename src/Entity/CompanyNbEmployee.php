<?php

namespace App\Entity;

use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass="App\Repository\CompanyNbEmployeeRepository")
 */
class CompanyNbEmployee
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="integer")
     */
    private $nbEmployee;

    /**
     * @ORM\OneToMany(targetEntity="App\Entity\Company", mappedBy="idCompanyNbEmployee")
     */
    private $companies;

    public function __construct()
    {
        $this->companies = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getNbEmployee(): ?int
    {
        return $this->nbEmployee;
    }

    public function setNbEmployee(int $nbEmployee): self
    {
        $this->nbEmployee = $nbEmployee;

        return $this;
    }

    /**
     * @return Collection|Company[]
     */
    public function getCompanies(): Collection
    {
        return $this->companies;
    }

    public function addCompany(Company $company): self
    {
        if (!$this->companies->contains($company)) {
            $this->companies[] = $company;
            $company->setIdCompanyNbEmployee($this);
        }

        return $this;
    }

    public function removeCompany(Company $company): self
    {
        if ($this->companies->contains($company)) {
            $this->companies->removeElement($company);
            // set the owning side to null (unless already changed)
            if ($company->getIdCompanyNbEmployee() === $this) {
                $company->setIdCompanyNbEmployee(null);
            }
        }

        return $this;
    }
}
